#%占位符
name='张三'
age=20
print('我叫%s，今年%d岁' %(name,age))
#{}占位符
print('我叫{0}，今年{1}岁'.format(name,age))
#f格式化
print(f'我叫{name}，今年{age}岁')
#展位宽度
print('%10d' % 99)
#保留精度
print('%.3f' % 3.1415926)       #小数点后3位
#同时保留宽度精度
print('%10.3f' % 3.1415926)     #总宽度10，小数点后3位
#{}站位
print('{0:.3}'.format(3.1415926))   #.3表示一共三位数
print('{0:.3f}'.format(3.1415926))   #.3f表示一共三位小数
print('{0:10.3f}'.format(3.1415926))    #总宽度10，小数点后3位

